export class userInfo {
  name: string;
  weekGoal: number;
  quarterGoal: number;
  weekTotal: number;
  quarterTotal: number;

  constructor(
    weekGoal: number,
    quarterGoal: number,
    weekTotal: number,
    quarterTotal: number
  ) {
    this.name = name;
    this.weekGoal = weekGoal;
    this.quarterGoal = quarterGoal;
    this.weekTotal = weekTotal;
    this.quarterTotal = quarterTotal;
  }
}
