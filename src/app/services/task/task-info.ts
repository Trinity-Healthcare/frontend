export class TaskInfo {
  taskName: String;
  photoRequired: String;
  pointValue: number;

  constructor(taskName: String, photoRequired: String, pointValue: number) {
    this.taskName = taskName;
    this.photoRequired = photoRequired;
    this.pointValue = pointValue;
  }
}

